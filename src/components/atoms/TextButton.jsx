import React from "react";
import Styled from "styled-components";

const StyledButton = Styled.button`
    color: ${props => props.theme.colors.white};
    background-color: ${props => props.theme.colors.purple};
    border-radius: 33px;
    margin-top: 2.5em;

    font-size: ${props => props.theme.fontSizes.mediumSmall};
    width: 22em;
    padding: 1em 0;
    border: 0px; 

    :focus {
        border: 0px solid ${props => props.theme.colors.lightBlue};
        outline: none;

        transition: all 0.25s ease;
        cursor: pointer;
    }

    :hover {
        border: 0px solid ${props => props.theme.colors.lightBlue};
        outline: none;

        filter: brightness(75%);
        transition: all 0.25s ease;
        cursor: pointer;
    }
`;

function TextButton({text, type, onClick, style}) {
    return (
        <StyledButton type={type} onClick={onClick} style={style}>{text}</StyledButton>
    );
}

export default TextButton;